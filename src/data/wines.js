export const wineTypes = ["all", "red", "white", "rosé", "sparkling"];

export function searchWines(query, type) {
  const q = query.toLowerCase();
  return wines.filter((wine) => {
    const matchesType = type === "all" || wine.type === type;
    const matchesQuery =
      !q ||
      wine.name.toLowerCase().includes(q) ||
      wine.grape.toLowerCase().includes(q) ||
      wine.region.toLowerCase().includes(q) ||
      wine.country.toLowerCase().includes(q);
    return matchesType && matchesQuery;
  });
}
